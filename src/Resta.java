/**
 * Clase que realiza la operacion de resta
 * @author Diego
 * @version 1.0
 */
public class Resta {
    
    /**
     * Resta dos numeros
     * @param a primer numero
     * @param b segundo numero
     * @return resultado de la resta
     */
    public double calcular(double a, double b) {
        return a - b;
    }
}
