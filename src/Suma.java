/**
 * Clase que realiza la operacion de suma
 * @author Diego
 * @version 1.0
 */
public class Suma {
    
    /**
     * Suma dos numeros
     * @param a primer numero
     * @param b segundo numero
     * @return resultado de la suma
     */
    public double calcular(double a, double b) {
        return a + b;
    }
}
